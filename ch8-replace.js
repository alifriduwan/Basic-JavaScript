// Methods replaceAll()

//replace() จะแทนที่เฉพาะคำแรกที่หาเจอ
let str = "ประยุทธ์เป็นนายกที่ดีที่สุด ประยุทธ์ซื่อสัตย์";
let newStr = str.replace("ประยุทธ์", "ธนอน");
console.log(newStr); //ธนอนเป็นนายกที่ดีที่สุด ประยุทธ์ซื่อสัตย์

//replaceAll() จะแทนทั้งหมดที่หาเจอ
let str2 = "ประยุทธ์เป็นนายกที่ดีที่สุด ประยุทธ์ซื่อสัตย์";
let newStr2 = str.replaceAll("ประยุทธ์", "ธนอน");
console.log(newStr2); //ธนอนเป็นนายกที่ดีที่สุด ธนอนซื่อสัตย์
