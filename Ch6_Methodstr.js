// trim ตัดช่องว่างด้านหน้าและด้านหลังทิ้ง

console.log(" Learning JavaScript ".trim()); // "Learning JavaScript"

// Method indexOf() and lastIndexOf() เช็คตำแหน่งข้อความนั้นๆว่าอยู่ index ไหน

console.log("foo_foo_foo".indexOf("foo")); //0 ตำแหน่งแรกที่พบ
console.log("foo_foo_foo".lastIndexOf("foo")); //8 ตำแหน่งสุดท้ายที่พบ
